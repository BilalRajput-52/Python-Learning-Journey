#  range() Function
 
# Definition:

# The range() function generates a sequence of numbers.

# Syntax:

# range(stop)
# range(start, stop)
# range(start, stop, step)

# Example 1
i=1
for i in range(5):
  print(i)
print("\n")

# Example 2
for i in range(1, 6):
  print(i)
print("\n")

# Example 3
for i in range(1, 11, 2):
  print(i)
print("\n")

# Example 4
i=10
for i in range(10, 0, -1):
  print(i)
