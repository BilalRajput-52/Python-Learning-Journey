# For Loop

# Definition:
# A for loop is used to iterate over a sequence.
# It executes a block of code once for each item.

# Syntax:
# for variable in sequence:
    # code

# Example 1
fruits = ["Apple", "Banana", "Mango"]

for fruit in fruits:
   print(fruit)
print("\n")

# Example 2
for letter in "Python":
  print(letter)
print("\n")

# Example 3
for number in (10, 20, 30):
  print(number)
