# Topic: Logical Operators
# Logical operators are used to combine
# multiple conditions.

# and
# or
# not

 
# AND Operator
age = 20
# citizen = True

if age >= 18 and age==20:
    print("Eligible for voting")
 
# OR Operator
 
salary = 25000

if salary > 50000 or salary > 20000:
    print("Condition True")

# NOT Operator
 
is_raining = False

if not is_raining:
    print("You can go outside")

age = 21

if not age==18:
    print("not operator")
