
# Returning a Number

def add(a, b):
    return a + b

result = add(10, 20)

print(result)

# Returning a Square

def square(number):
    return number * number

print(square(5))

# Returning a String


def greet(name):
    return "Hello " + name

message = greet("Bilal")

print(message)

# Returning Multiple Values
 
def student(name,age):
    return name, age 
    
std= student("Bilal",22)
print(std)
 