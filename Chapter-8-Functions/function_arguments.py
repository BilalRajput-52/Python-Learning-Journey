# One Argument

def greet(name):
    print("Hello", name)

greet("Bilal")
print("Thank You\n")
# Multiple Arguments

def student(name, age):
    print("Name:", name)
    print("Age:", age)

student("Bilal", 22)
print("Thank You\n")
# Positional Arguments
 
def info(name, city):
    print(name)
    print(city)

info("Bilal", "Rahim Yar Khan")
print("Thank You\n")
# Real World Example

def calculate_total(price, quantity):
    print("Total =", price * quantity)

calculate_total(500, 3)
print("Thank You\n")