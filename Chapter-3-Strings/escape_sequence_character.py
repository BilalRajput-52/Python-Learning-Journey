  

# Chapter 3 - Strings in Python

# Topic: Escape Sequence Characters
 

# Definition:

# Escape Sequence Characters are special characters

# that start with a backslash \ and have a special

# meaning inside strings.

#

# They are used for formatting text and displaying

# special characters.

 

# Common Escape Sequences

# \n  -> New Line

# \t  -> Tab Space

# \" Bilal\" -> Double Quote

# bilal\'s  -> Single Quote

# \\  -> Backslash

 

# Example 1: New Line (\n)

print("Hello\nBilal")

# Output:

# Hello

# Bilal
 

# Example 2: Tab Space (\t)

print("Python\tProgramming")

# Output:

# Python    Programming


# Example 3: Double Quote (")

print("Bilal said \"Python is easy\"")

# Output:

# Bilal said "Python is easy"

 
# Example 4: Single Quote (')

print('Bilal\'s Book')

# Output:

# Bilal's Book


# Example 5: Backslash (\)

print("C:\\Users\\Bilal")

# Output:

# C:\Users\Bilal

# Complete Example
 

print("\nComplete Example\n")

