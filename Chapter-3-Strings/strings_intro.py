 

# Chapter 3 - Strings in Python
 
# Definition:

# A string is a sequence of characters enclosed in quotes.

# Strings are used to store text data.

# String Examples

name = "Bilal"
city = 'Lahore'

message = """
Hello!
Welcome to Python Strings.
"""

# Printing Strings

print("Name:", name)
print("City:", city)
print("Message:", message)

# Checking Data Type

print("Type of name:", type(name))

# Strings are Immutable

# This means we cannot change individual characters directly.

# name[0] = "M"    # This will cause an error

# But we can assign a new string

name = "Ahmed"

print("Updated Name:", name)
