 
# String Functions in Python
 

name = "Bilal Ahmed"

print("Original String:", name)

print("\nLength:", len(name))

print("\nStarts With:")
print(name.startswith("Bil"))

print("\nEnds With:")
print(name.endswith("med"))

print("\nCount:")
print(name.count("a"))

print("\nFind:")
print(name.find("Ahmed"))

print("\nReplace:")
print(name.replace("Bilal", "Muhammad"))

print("\nUpper:")
print(name.upper())

print("\nLower:")
print(name.lower())

print("\nCapitalize:")
print(name.capitalize())

print("\nTitle:")
print(name.title())