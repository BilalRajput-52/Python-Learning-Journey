# Chapter 4 - Lists in Python
 
# Definition:
# A list is an ordered collection of items.
# Lists are enclosed in square brackets [].

students = ["Ali", "Bilal", "Ahmed", "Usman"]

print("List:", students)
print(type(students))

print("First Student :", students[0])
print("Second Student:", students[1])

# Lists are Mutable

students[0] = "Hamza"

print("Updated List:", students)

# Different Data Types

data = ["Bilal", 21, 3.5, True]

print("Mixed List:", data)
