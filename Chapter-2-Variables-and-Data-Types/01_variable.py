 
# Variables in Python
 
# Definition:
# A variable is like a container that stores data.
# It is a name given to a memory location in a program.
# We use variables to store and manage values.

# Identifier:
# The name of a variable is called an identifier.
# It helps Python identify which variable we are referring to.

# Examples of Variables

a = 8              # Integer variable
b = "Bilal"        # String variable
c = 45.7           # Float variable

# Printing the values stored in variables

print("Value of a:", a)
print("Value of b:", b)
print("Value of c:", c)

# Checking data types

print("Data type of a:", type(a))
print("Data type of b:", type(b))
print("Data type of c:", type(c))